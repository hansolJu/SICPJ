//OPTAB: 연상 명령어를 찾아서 기계어를 번역하는데 사용
//1. 적어도 명령어 코드와 해당되는 기계 코들를 포함
//2. 명령어 형식과 길이에 대한 정보를 포함하기도 함(서로 다른 명령어를 갖는 기계)
//3. 패스 1에서 원시 프로그램의 명령 코드를 조사하고 확인하는데 사용
